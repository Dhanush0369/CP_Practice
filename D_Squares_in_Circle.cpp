void solve(){
    int r;cin>>r;
    int ans=1;
    ans += (r-1)*4;

    int sum=0,i=1;
    while(r-i>0){
        int tem = 2*(r-i)-1;
        sum+=tem;
        i+=2;
    }
    sum = sum*4;
    ans +=sum;
    cout<<ans;
}

